public class SentenciaSwitch {
    public static void main(String[] args) {
        int opcion = 9;

        switch(opcion){
            case 1:
                System.out.println("opcion 1");
                break;
            case 2:
                System.out.println("Opcion 2");
            case 3:
                System.out.println("Opcion 3");
                break;
            default:
                System.out.println("Opcion invalida");
                break;

        }
    }
}
